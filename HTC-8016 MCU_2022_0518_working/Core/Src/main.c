void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{   
    switch(GPIO_Pin)
    {
        case GPIO_PIN_3:
        {
        	if(ERR_NO != NO_ERR)	{ return; }
        	
        	if(g_opt.exp_opt == PPS)
        	{
        		switch(Hand_Switch_Status)
        		{        
        			case sw_EXP_ing:
        			{
        				FIL_ON;
        				if(PpsXrayStatus == _OFF)
        				{	
        				        if(g_xray.stat == 0)
        				        {
                        				g_xray.stat = 1;				                        				
                        				T_1ms_Exp_Err_chk = 0;
        				        }
        				        
        					T_1ms_Exp_Pulse_Err_chk = 0;
        				
        					EXP_DTC_ON;
        					PPS_MODE_OFF;
        					HAL_DAC_SetValue(&hdac, DAC_CHANNEL_1, DAC_ALIGN_12B_R, g_xray.pron_dac);  //mA DAC
                                                HAL_DAC_SetValue(&hdac, DAC_CHANNEL_2, DAC_ALIGN_12B_R, g_xray.kv_dac);  //kV DAC
                                                if(DipSwitch.bit.sw3_buzzeroff == DIPSW_OFF) { BuzzerState = _ON; }
                                                EXPOSURE_LED_ON;

                                                PpsXrayStatus = _ON;
        				}
        				else if(PpsXrayStatus == _ON)
        				{
        					EXP_DTC_OFF;
        					PPS_MODE_ON;
        					HAL_DAC_SetValue(&hdac, DAC_CHANNEL_1, DAC_ALIGN_12B_R, g_xray.prof_dac);  //mA DAC
                                                HAL_DAC_SetValue(&hdac, DAC_CHANNEL_2, DAC_ALIGN_12B_R, 0);  //kV DAC
                                                BuzzerState = _OFF;
                                                EXPOSURE_LED_OFF;

                                                PpsXrayStatus = _OFF;
        				}
                        
                                }
                                break;
                        }
                }
        }    
        
        case GPIO_PIN_4:
        {
                //CAN_RX_Queue_Add();                
                break;
        }
    }   
}
